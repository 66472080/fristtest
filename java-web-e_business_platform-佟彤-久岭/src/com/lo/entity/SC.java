package com.lo.entity;

public class SC {
	private int sid;
	private int oid;
	private String suser;
	private String dese;
	private double price;
	private int buyticket;
	private double menoy;
	private int uid;
	
	
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSuser() {
		return suser;
	}
	public void setSuser(String suser) {
		this.suser = suser;
	}
	public String getDese() {
		return dese;
	}
	public void setDese(String dese) {
		this.dese = dese;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public int getBuyticket() {
		return buyticket;
	}
	public void setBuyticket(int buyticket) {
		this.buyticket = buyticket;
	}
	public double getMenoy() {
		return menoy;
	}
	public void setMenoy(double menoy) {
		this.menoy = menoy;
	}
	
}
